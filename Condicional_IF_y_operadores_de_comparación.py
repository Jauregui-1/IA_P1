# Se definen dos variables num1 y num2 con valores 15 y 20, respectivamente
num1 = 15
num2 = 20

# Se evalúa una condición: si num1 es menor que num2
# En este caso, 15 es menor que 20, por lo que se ejecuta el bloque del if
if num1 < num2:
    print('Se ejecuta el if.')

# Se definen dos nuevas variables num1 y num2 con valores 1450 y 60, respectivamente
