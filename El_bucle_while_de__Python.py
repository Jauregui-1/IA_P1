# Se imprime un mensaje indicando el inicio del primer bucle while
print('Primer bucle while')

# Se inicializa la variable x con valor 0
x = 0
# Se inicia un bucle while que se ejecuta mientras x sea menor o igual a 15
while x <= 15:
    # Se imprime el valor actual de x
    print(x)
    # Se incrementa x en 5 en cada iteración del bucle
    x += 5

# Se imprime una línea en blanco para separar las salidas de los bucles
print('\n')

