# Se asigna la cadena 'ROJO' a la variable 'color'
color = 'ROJO'

# Se evalúa una condición: si 'color' es igual a 'ROJO'
# En este caso, 'color' es igual a 'ROJO', por lo que se ejecuta el bloque del if
if color == 'ROJO':
    print('El color es ROJO')
# Si 'color' no fuera igual a 'ROJO', se ejecutaría el bloque del else
else:
    print('El color no es ROJO')
