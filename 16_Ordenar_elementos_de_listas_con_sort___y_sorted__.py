# Se crea una lista de colores con 10 elementos
colores = ['rojo', 'azul', 'verde', 'amarillo', 'marron', 'lila', 'negro', 'rosa', 'blanco', 'naranja']

# Se utiliza el método sort() con el argumento reverse=True para ordenar la lista en orden descendente
# Esto reorganiza la lista de colores de la Z a la A
colores.sort(reverse=True)

# Se imprime la lista 'colores' después de haber sido ordenada en orden descendente
print(colores)
