# Se crea una lista de colores con 10 elementos
colores = ['rojo', 'azul', 'verde', 'amarillo', 'marron', 'lila', 'negro', 'rosa', 'blanco', 'naranja']

# Se utilizan los métodos append() para añadir nuevos elementos al final de la lista
# - colores.append('fuxia') añade el elemento 'fuxia' al final de la lista
# - colores.append('celeste') añade el elemento 'celeste' al final de la lista
colores.append('fuxia'), colores.append('celeste')

# Se imprime la lista 'colores' después de añadir los nuevos elementos
print(colores)
