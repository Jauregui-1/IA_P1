# Se crea una lista de colores con 10 elementos
colores = ['rojo', 'azul', 'verde', 'amarillo', 'marron', 'lila', 'negro', 'rosa', 'blanco', 'naranja']

# Se utilizan los métodos insert() para añadir nuevos elementos en posiciones específicas
# - colores.insert(-4, 'magenta') inserta el elemento 'magenta' en la posición -4 (cuarta desde el final)
# - colores.insert(-1, 'turquesa') inserta el elemento 'turquesa' en la posición -1 (penúltimo lugar)
colores.insert(-4, 'magenta'), colores.insert(-1, 'turquesa')

# Se imprime la lista 'colores' después de añadir los nuevos elementos en las posiciones especificadas
print(colores)
