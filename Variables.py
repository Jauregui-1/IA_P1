# Definimos una variable llamada 'mensaje' que almacena el texto "Buenos días." 
# El '\t' al final añade un espacio de tabulación (más espacio entre palabras).
mensaje = "Buenos dias.\t"

# Definimos una variable llamada 'mensaje1' que almacena el texto "Que tenga lindo día."
mensaje1 = "Que tenga lindo dia."

# Definimos una variable numérica llamada 'num01' y le asignamos el valor 100.
num01 = 100

# Definimos otra variable numérica llamada 'num02' y le asignamos el valor 50.
num02 = 50

# Restamos el valor de 'num02' al valor de 'num01'. El resultado (100 - 50 = 50) 
# se almacena en la variable 'resta'.
resta = num01 - num02

# Imprimimos los valores de las variables 'mensaje' y 'mensaje1'.
# El contenido de 'mensaje' y 'mensaje1' se mostrará en la misma línea con un espacio intermedio.
# Además, el '\t' en 'mensaje' añadirá un espacio extra entre los textos.
print(mensaje, mensaje1)

# Sumamos 'num01' y 'num02' (100 + 50 = 150) y mostramos el resultado en pantalla.
print(num01 + num02)

# Imprimimos el valor almacenado en 'resta', que es el resultado de la resta anterior (50).
print(resta)
